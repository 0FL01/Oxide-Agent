/// Stable semantic kind for an entry stored in hot agent memory.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq, Default)]
pub enum AgentMessageKind {
    /// Backward-compatible fallback for messages persisted before Stage 2.
    #[default]
    Legacy,
    /// Generic system context injected into the run.
    SystemContext,
    /// Topic-scoped pinned `AGENTS.md` content.
    TopicAgentsMd,
    /// Primary user task for the current run.
    UserTask,
    /// Additional user context injected while a run is active.
    RuntimeContext,
    /// Generic user turn without stronger typing metadata.
    UserTurn,
    /// Final or intermediate assistant answer.
    AssistantResponse,
    /// Assistant answer that contains hidden reasoning content.
    AssistantReasoning,
    /// Assistant message that requests tool execution.
    AssistantToolCall,
    /// Raw tool result retained in hot memory.
    ToolResult,
    /// Dynamically loaded skill instructions.
    SkillContext,
    /// System replay instruction after operator approval.
    ApprovalReplay,
    /// System status about topic-scoped infra / approvals.
    InfraStatus,
    /// Summary generated by compaction.
    Summary,
    /// Lightweight placeholder pointing at archived context.
    ArchiveReference,
}

/// Retention class used by future compaction policies.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
pub enum CompactionRetention {
    /// Must stay raw in hot context and never be compacted.
    Pinned,
    /// Keep raw while the work is still active.
    ProtectedLive,
    /// Large artifacts that should be pruned or externalized before summary.
    PrunableArtifact,
    /// Historical conversation content eligible for summary compaction.
    CompactableHistory,
}

impl AgentMessageKind {
    /// Default retention class for this memory entry kind.
    #[must_use]
    pub const fn retention(self) -> CompactionRetention {
        match self {
            Self::TopicAgentsMd | Self::Summary | Self::ArchiveReference => {
                CompactionRetention::Pinned
            }
            Self::SystemContext
            | Self::UserTask
            | Self::RuntimeContext
            | Self::SkillContext
            | Self::ApprovalReplay
            | Self::InfraStatus => CompactionRetention::ProtectedLive,
            Self::ToolResult => CompactionRetention::PrunableArtifact,
            Self::Legacy
            | Self::UserTurn
            | Self::AssistantResponse
            | Self::AssistantReasoning
            | Self::AssistantToolCall => CompactionRetention::CompactableHistory,
        }
    }
}

/// Resolve the retention class for a message, allowing tool-aware overrides.
#[must_use]
pub fn resolve_retention(kind: AgentMessageKind, tool_name: Option<&str>) -> CompactionRetention {
    match kind {
        AgentMessageKind::ToolResult if is_summary_preferred_tool(tool_name) => {
            CompactionRetention::CompactableHistory
        }
        _ => kind.retention(),
    }
}

fn is_summary_preferred_tool(tool_name: Option<&str>) -> bool {
    matches!(tool_name, Some("delegate_to_sub_agent"))
}

/// Trigger point for a compaction pipeline invocation.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
pub enum CompactionTrigger {
    /// Compaction check before the first model request of a run.
    PreRun,
    /// Compaction check before a later loop iteration.
    PreIteration,
    /// Explicit manual compaction requested by the operator or transport.
    Manual,
    /// Cleanup and compaction housekeeping after a task reaches a final answer.
    PostRun,
}

/// Static policy knobs for the compaction subsystem.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CompactionPolicy {
    /// Percent of the window where warning-level telemetry should start.
    pub warning_threshold_percent: u8,
    /// Percent of the window where pruning becomes desirable.
    pub prune_threshold_percent: u8,
    /// Percent of the window where compaction becomes desirable.
    pub compact_threshold_percent: u8,
    /// Percent of the window treated as an over-limit safety cap.
    pub over_limit_threshold_percent: u8,
    /// Reserved buffer kept free beyond the response budget.
    pub hard_reserve_tokens: usize,
    /// Minimum approximate token size before a tool payload is externalized.
    pub externalize_threshold_tokens: usize,
    /// Minimum character size before a tool payload is externalized.
    pub externalize_threshold_chars: usize,
    /// Maximum preview size kept inline after externalization.
    pub externalize_preview_chars: usize,
    /// Minimum approximate token size before an old tool payload is pruned.
    pub prune_min_tokens: usize,
    /// Minimum character size before an old tool payload is pruned.
    pub prune_min_chars: usize,
    /// Maximum preview size kept inline after pruning.
    pub prune_preview_chars: usize,
    /// Token budget reserved for the recent raw tool window before older tool
    /// artifacts become eligible for pruning or summary compaction.
    pub protected_tool_window_tokens: usize,
}

/// Configured hot-context thresholds for warning and compaction.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct HotContextLimits {
    /// Token budget at which the hook emits a soft warning.
    pub soft_warning_tokens: usize,
    /// Token budget at which the hook requests compaction.
    pub hard_compaction_tokens: usize,
}

impl HotContextLimits {
    /// Create hot-context limits, ensuring the hard limit stays above the soft limit.
    #[must_use]
    pub const fn new(soft_warning_tokens: usize, hard_compaction_tokens: usize) -> Self {
        let hard_compaction_tokens = if hard_compaction_tokens > soft_warning_tokens {
            hard_compaction_tokens
        } else {
            soft_warning_tokens.saturating_add(1)
        };

        Self {
            soft_warning_tokens,
            hard_compaction_tokens,
        }
    }
}

impl Default for HotContextLimits {
    fn default() -> Self {
        Self::new(
            DEFAULT_HOT_CONTEXT_SOFT_WARNING_TOKENS,
            DEFAULT_HOT_CONTEXT_HARD_COMPACTION_TOKENS,
        )
    }
}

impl Default for CompactionPolicy {
    fn default() -> Self {
        Self {
            warning_threshold_percent: 65,
            prune_threshold_percent: 75,
            compact_threshold_percent: 85,
            over_limit_threshold_percent: 95,
            hard_reserve_tokens: 8_192,
            externalize_threshold_tokens: 512,
            externalize_threshold_chars: 2_048,
            externalize_preview_chars: 280,
            prune_min_tokens: 128,
            prune_min_chars: 512,
            prune_preview_chars: 160,
            protected_tool_window_tokens: get_compaction_protected_tool_window_tokens(),
        }
    }
}

