//! Shared types for Agent Mode context compaction.

use super::archive::ArchiveRef;
use crate::llm::ToolDefinition;
use serde::{Deserialize, Serialize};

/// Stable semantic kind for an entry stored in hot agent memory.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq, Default)]
pub enum AgentMessageKind {
    /// Backward-compatible fallback for messages persisted before Stage 2.
    #[default]
    Legacy,
    /// Generic system context injected into the run.
    SystemContext,
    /// Topic-scoped pinned `AGENTS.md` content.
    TopicAgentsMd,
    /// Primary user task for the current run.
    UserTask,
    /// Additional user context injected while a run is active.
    RuntimeContext,
    /// Generic user turn without stronger typing metadata.
    UserTurn,
    /// Final or intermediate assistant answer.
    AssistantResponse,
    /// Assistant answer that contains hidden reasoning content.
    AssistantReasoning,
    /// Assistant message that requests tool execution.
    AssistantToolCall,
    /// Raw tool result retained in hot memory.
    ToolResult,
    /// Dynamically loaded skill instructions.
    SkillContext,
    /// System replay instruction after operator approval.
    ApprovalReplay,
    /// System status about topic-scoped infra / approvals.
    InfraStatus,
    /// Summary generated by compaction.
    Summary,
    /// Lightweight placeholder pointing at archived context.
    ArchiveReference,
}

/// Retention class used by future compaction policies.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
pub enum CompactionRetention {
    /// Must stay raw in hot context and never be compacted.
    Pinned,
    /// Keep raw while the work is still active.
    ProtectedLive,
    /// Large artifacts that should be pruned or externalized before summary.
    PrunableArtifact,
    /// Historical conversation content eligible for summary compaction.
    CompactableHistory,
}

impl AgentMessageKind {
    /// Default retention class for this memory entry kind.
    #[must_use]
    pub const fn retention(self) -> CompactionRetention {
        match self {
            Self::TopicAgentsMd | Self::Summary | Self::ArchiveReference => {
                CompactionRetention::Pinned
            }
            Self::SystemContext
            | Self::UserTask
            | Self::RuntimeContext
            | Self::SkillContext
            | Self::ApprovalReplay
            | Self::InfraStatus => CompactionRetention::ProtectedLive,
            Self::ToolResult => CompactionRetention::PrunableArtifact,
            Self::Legacy
            | Self::UserTurn
            | Self::AssistantResponse
            | Self::AssistantReasoning
            | Self::AssistantToolCall => CompactionRetention::CompactableHistory,
        }
    }
}

/// Trigger point for a compaction pipeline invocation.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
pub enum CompactionTrigger {
    /// Compaction check before the first model request of a run.
    PreRun,
    /// Compaction check before a later loop iteration.
    PreIteration,
    /// Explicit manual compaction requested by the operator or transport.
    Manual,
}

/// Static policy knobs for the compaction subsystem.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CompactionPolicy {
    /// Percent of the window where warning-level telemetry should start.
    pub warning_threshold_percent: u8,
    /// Percent of the window where pruning becomes desirable.
    pub prune_threshold_percent: u8,
    /// Percent of the window where compaction becomes desirable.
    pub compact_threshold_percent: u8,
    /// Percent of the window treated as an over-limit safety cap.
    pub over_limit_threshold_percent: u8,
    /// Reserved buffer kept free beyond the response budget.
    pub hard_reserve_tokens: usize,
    /// Minimum approximate token size before a tool payload is externalized.
    pub externalize_threshold_tokens: usize,
    /// Minimum character size before a tool payload is externalized.
    pub externalize_threshold_chars: usize,
    /// Maximum preview size kept inline after externalization.
    pub externalize_preview_chars: usize,
    /// Minimum approximate token size before an old tool payload is pruned.
    pub prune_min_tokens: usize,
    /// Minimum character size before an old tool payload is pruned.
    pub prune_min_chars: usize,
    /// Maximum preview size kept inline after pruning.
    pub prune_preview_chars: usize,
}

impl Default for CompactionPolicy {
    fn default() -> Self {
        Self {
            warning_threshold_percent: 65,
            prune_threshold_percent: 75,
            compact_threshold_percent: 85,
            over_limit_threshold_percent: 95,
            hard_reserve_tokens: 8_192,
            externalize_threshold_tokens: 512,
            externalize_threshold_chars: 2_048,
            externalize_preview_chars: 280,
            prune_min_tokens: 128,
            prune_min_chars: 512,
            prune_preview_chars: 160,
        }
    }
}

/// Agent/session scope metadata used for archive and payload keys.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CompactionScope {
    /// Stable context key for topic/thread/session scope.
    pub context_key: String,
    /// Stable flow identifier within the context.
    pub flow_id: String,
}

impl Default for CompactionScope {
    fn default() -> Self {
        Self {
            context_key: "unknown-context".to_string(),
            flow_id: "hot-memory".to_string(),
        }
    }
}

/// Immutable request payload describing a compaction checkpoint.
#[derive(Debug, Clone)]
pub struct CompactionRequest<'a> {
    /// Why the pipeline was invoked.
    pub trigger: CompactionTrigger,
    /// User-visible task text.
    pub task: &'a str,
    /// Current fully rendered system prompt.
    pub system_prompt: &'a str,
    /// Tool definitions exposed to the model.
    pub tools: &'a [ToolDefinition],
    /// Active model name for the main agent request.
    pub model_name: &'a str,
    /// Configured response token budget for the active model.
    pub model_max_output_tokens: u32,
    /// Whether the current execution is a sub-agent.
    pub is_sub_agent: bool,
}

impl<'a> CompactionRequest<'a> {
    /// Build a request for a compaction checkpoint.
    #[must_use]
    pub const fn new(
        trigger: CompactionTrigger,
        task: &'a str,
        system_prompt: &'a str,
        tools: &'a [ToolDefinition],
        model_name: &'a str,
        model_max_output_tokens: u32,
        is_sub_agent: bool,
    ) -> Self {
        Self {
            trigger,
            task,
            system_prompt,
            tools,
            model_name,
            model_max_output_tokens,
            is_sub_agent,
        }
    }
}

/// Health state derived from the estimated full request budget.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum BudgetState {
    /// Plenty of room remains in the request budget.
    Healthy,
    /// Context is growing and should be surfaced to telemetry.
    Warning,
    /// Pruning bulky artifacts would likely help.
    ShouldPrune,
    /// History compaction should run before the next model call.
    ShouldCompact,
    /// The projected request would exceed the configured window.
    OverLimit,
}

/// Token accounting grouped by hot-memory retention class.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct HotMemoryBudget {
    /// Total estimated tokens represented by the current hot memory.
    pub total_tokens: usize,
    /// Total messages represented in hot memory.
    pub total_messages: usize,
    /// Tokens belonging to pinned messages.
    pub pinned_tokens: usize,
    /// Tokens belonging to protected live messages.
    pub protected_live_tokens: usize,
    /// Tokens belonging to prunable artifact messages.
    pub prunable_artifact_tokens: usize,
    /// Tokens belonging to compactable history messages.
    pub compactable_history_tokens: usize,
    /// Tokens specifically attributed to skill context messages.
    pub skill_context_tokens: usize,
    /// Tokens specifically attributed to runtime context messages.
    pub runtime_context_tokens: usize,
}

/// Full request budget estimate for a compaction checkpoint.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct BudgetEstimate {
    /// Configured effective context window for Agent Mode.
    pub context_window_tokens: usize,
    /// Estimated token count of the rendered system prompt.
    pub system_prompt_tokens: usize,
    /// Estimated token count of serialized tool schemas.
    pub tool_schema_tokens: usize,
    /// Estimated token count of the active hot memory.
    pub hot_memory: HotMemoryBudget,
    /// Total tokens currently attributed to loaded skills.
    pub loaded_skill_tokens: usize,
    /// Configured response token reserve for the active model.
    pub reserved_output_tokens: usize,
    /// Additional hard safety buffer kept free.
    pub hard_reserve_tokens: usize,
    /// Estimated input-side request size before model completion tokens.
    pub total_input_tokens: usize,
    /// Estimated full request size including completion reserve and hard reserve.
    pub projected_total_tokens: usize,
    /// Remaining headroom in the configured context window.
    pub headroom_tokens: usize,
    /// Warning threshold derived from policy and window size.
    pub warning_threshold_tokens: usize,
    /// Prune threshold derived from policy and window size.
    pub prune_threshold_tokens: usize,
    /// Compact threshold derived from policy and window size.
    pub compact_threshold_tokens: usize,
    /// Over-limit threshold derived from policy and window size.
    pub over_limit_threshold_tokens: usize,
    /// High-level budget state.
    pub state: BudgetState,
}

/// Per-entry classifier result for the current hot memory snapshot.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ClassifiedMemoryEntry {
    /// Stable position within hot memory.
    pub index: usize,
    /// Resolved semantic message kind.
    pub kind: AgentMessageKind,
    /// Classifier bucket assigned to this entry.
    pub retention: CompactionRetention,
    /// Estimated tokens represented by this entry.
    pub estimated_tokens: usize,
    /// Character count of the visible message content.
    pub content_chars: usize,
    /// Whether hidden reasoning payload is attached.
    pub has_reasoning: bool,
    /// Tool name for tool-related entries when available.
    pub tool_name: Option<String>,
    /// Whether the payload already lives outside hot memory.
    pub is_externalized: bool,
    /// Artifact reference when the payload has been externalized.
    pub archive_ref: Option<ArchiveRef>,
    /// Whether the artifact was already pruned to a placeholder.
    pub is_pruned: bool,
    /// Whether this entry belongs to the recent raw working window.
    pub preserve_in_raw_window: bool,
}

/// Result of replacing large inline payloads with lightweight artifact references.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct ExternalizationOutcome {
    /// Whether any hot-memory entries were rewritten.
    pub applied: bool,
    /// Number of messages externalized during this checkpoint.
    pub externalized_count: usize,
    /// Hot-memory token estimate removed from raw payloads.
    pub reclaimed_tokens: usize,
    /// Total visible characters removed from hot memory.
    pub reclaimed_chars: usize,
    /// Artifact refs created during this checkpoint.
    pub archive_refs: Vec<ArchiveRef>,
}

/// Result of archiving displaced hot-memory chunks for future retrieval features.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct ArchivePersistenceOutcome {
    /// Whether archive persistence was attempted for this checkpoint.
    pub attempted: bool,
    /// Number of archived chunks written or prepared.
    pub archived_chunk_count: usize,
    /// Number of original hot-memory messages represented in archived chunks.
    pub archived_message_count: usize,
    /// Archive refs created for displaced chunks.
    pub archive_refs: Vec<ArchiveRef>,
}

/// Result of pruning older tool artifacts after protecting the live raw window.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct PruneOutcome {
    /// Whether any hot-memory entries were rewritten.
    pub applied: bool,
    /// Number of messages pruned during this checkpoint.
    pub pruned_count: usize,
    /// Hot-memory token estimate removed from raw payloads.
    pub reclaimed_tokens: usize,
    /// Total visible characters removed from hot memory.
    pub reclaimed_chars: usize,
    /// Indices of pruned messages in hot memory order before rebuild.
    pub pruned_indices: Vec<usize>,
}

/// Structured summary generated for compactable history.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq, Default)]
pub struct CompactionSummary {
    /// Overall goal inferred from the compacted history.
    pub goal: String,
    /// Constraints that must stay true while work continues.
    pub constraints: Vec<String>,
    /// Decisions already made in the compacted history.
    pub decisions: Vec<String>,
    /// Discoveries from exploration and prior execution.
    pub discoveries: Vec<String>,
    /// Relevant files, entities, commands, or identifiers.
    pub relevant_files_entities: Vec<String>,
    /// Remaining work handed forward after compaction.
    pub remaining_work: Vec<String>,
    /// Risks, warnings, or open questions.
    pub risks: Vec<String>,
}

/// Result of generating a structured summary from compactable history.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct SummaryGenerationOutcome {
    /// Whether the summarizer ran for this checkpoint.
    pub attempted: bool,
    /// Whether the deterministic fallback path was used.
    pub used_fallback: bool,
    /// Model name used when the LLM path succeeded.
    pub model_name: Option<String>,
    /// Structured summary returned by the summarizer.
    pub summary: Option<CompactionSummary>,
}

/// Result of rebuilding hot memory into pinned, live, summary, and recent-raw slices.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct RebuildOutcome {
    /// Whether hot memory was rewritten into a new staged layout.
    pub applied: bool,
    /// Whether a structured summary entry was inserted or refreshed.
    pub inserted_summary: bool,
    /// Whether an archive reference entry was inserted.
    pub inserted_archive_reference: bool,
    /// Number of original hot-memory messages removed during rebuild.
    pub dropped_message_count: usize,
    /// Stable indices dropped from the pre-rebuild memory ordering.
    pub dropped_indices: Vec<usize>,
    /// Stable indices preserved as the verbatim recent raw window.
    pub preserved_recent_indices: Vec<usize>,
}

/// Aggregate stats for one classifier bucket.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct CompactionClassSummary {
    /// Number of entries in this bucket.
    pub message_count: usize,
    /// Total estimated tokens in this bucket.
    pub token_count: usize,
    /// Stable hot-memory indices for the bucket entries.
    pub message_indices: Vec<usize>,
}

/// Recent raw window that should stay verbatim even when older history compacts.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct RecentRawWindow {
    /// Most recent user turns kept verbatim.
    pub user_turn_indices: Vec<usize>,
    /// Most recent assistant turns kept verbatim.
    pub assistant_turn_indices: Vec<usize>,
    /// Most recent tool interaction entries kept verbatim.
    pub tool_interaction_indices: Vec<usize>,
}

impl RecentRawWindow {
    /// Returns true when the given hot-memory index is part of the raw window.
    #[must_use]
    pub fn contains(&self, index: usize) -> bool {
        self.user_turn_indices.contains(&index)
            || self.assistant_turn_indices.contains(&index)
            || self.tool_interaction_indices.contains(&index)
    }
}

/// Snapshot of the current hot memory split into compaction classes.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct CompactionSnapshot {
    /// Per-entry classifier results in stable memory order.
    pub entries: Vec<ClassifiedMemoryEntry>,
    /// Entries that must remain pinned.
    pub pinned: CompactionClassSummary,
    /// Entries that must remain raw while the run is active.
    pub protected_live: CompactionClassSummary,
    /// Bulky artifacts that should be pruned or externalized first.
    pub prunable_artifacts: CompactionClassSummary,
    /// Older working history that is eligible for summary compaction.
    pub compactable_history: CompactionClassSummary,
    /// Recent raw turns/tool interactions kept verbatim.
    pub recent_raw_window: RecentRawWindow,
}

/// Observable result of a compaction checkpoint.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CompactionOutcome {
    /// Trigger that produced this outcome.
    pub trigger: CompactionTrigger,
    /// Whether the pipeline mutated hot memory.
    pub applied: bool,
    /// Token count before the pipeline ran.
    pub token_count_before: usize,
    /// Token count after the pipeline ran.
    pub token_count_after: usize,
    /// Full budget estimate collected at this checkpoint.
    pub budget: BudgetEstimate,
    /// Classified view of the current hot memory.
    pub snapshot: CompactionSnapshot,
    /// Result of payload externalization applied before later stages.
    pub externalization: ExternalizationOutcome,
    /// Result of archive persistence for displaced compacted history chunks.
    pub archive_persistence: ArchivePersistenceOutcome,
    /// Result of old artifact pruning applied before summary compaction.
    pub pruning: PruneOutcome,
    /// Structured summary prepared for future rebuild stages.
    pub summary_generation: SummaryGenerationOutcome,
    /// Result of rebuilding hot memory after summary compaction.
    pub rebuild: RebuildOutcome,
}

impl CompactionOutcome {
    /// Build a no-op outcome.
    #[must_use]
    pub fn noop(
        trigger: CompactionTrigger,
        budget: BudgetEstimate,
        snapshot: CompactionSnapshot,
    ) -> Self {
        let token_count = budget.hot_memory.total_tokens;
        Self {
            trigger,
            applied: false,
            token_count_before: token_count,
            token_count_after: token_count,
            budget,
            snapshot,
            externalization: ExternalizationOutcome::default(),
            archive_persistence: ArchivePersistenceOutcome::default(),
            pruning: PruneOutcome::default(),
            summary_generation: SummaryGenerationOutcome::default(),
            rebuild: RebuildOutcome::default(),
        }
    }
}

#[cfg(test)]
mod tests {
    use super::{AgentMessageKind, CompactionPolicy, CompactionRetention};

    #[test]
    fn topic_agents_md_is_pinned() {
        assert_eq!(
            AgentMessageKind::TopicAgentsMd.retention(),
            CompactionRetention::Pinned
        );
    }

    #[test]
    fn tool_results_are_prunable() {
        assert_eq!(
            AgentMessageKind::ToolResult.retention(),
            CompactionRetention::PrunableArtifact
        );
    }

    #[test]
    fn live_context_entries_are_protected() {
        assert_eq!(
            AgentMessageKind::UserTask.retention(),
            CompactionRetention::ProtectedLive
        );
        assert_eq!(
            AgentMessageKind::SkillContext.retention(),
            CompactionRetention::ProtectedLive
        );
    }

    #[test]
    fn default_policy_keeps_thresholds_ordered() {
        let policy = CompactionPolicy::default();
        assert!(policy.warning_threshold_percent < policy.prune_threshold_percent);
        assert!(policy.prune_threshold_percent < policy.compact_threshold_percent);
        assert!(policy.compact_threshold_percent < policy.over_limit_threshold_percent);
        assert!(policy.hard_reserve_tokens > 0);
    }
}
